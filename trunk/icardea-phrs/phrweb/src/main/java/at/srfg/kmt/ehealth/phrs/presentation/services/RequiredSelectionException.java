package at.srfg.kmt.ehealth.phrs.presentation.services;

@SuppressWarnings("serial")
public class RequiredSelectionException extends Exception {

	//private static final long serialVersionUID = 1181558295271634121L;

	public RequiredSelectionException() {
		super();
	}

	public RequiredSelectionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RequiredSelectionException(String arg0) {
		super(arg0);
	}

	public RequiredSelectionException(Throwable arg0) {
		super(arg0);
	}

}
